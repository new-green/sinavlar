void main() {
  /*
  ------Aritmatik operatörler------
  -------Ön ek operatörleri-------
  Ön ek operatröleri adından da anlaşılacağı üzere değişkenin yazımından önce kullanılır.
  */
  var a = 10;
  print(-a);
  /*
 ---"-a"---
 Bu bloktaki kullanım "a" değerini negatif yapmıştır.
 Ama "+a" gibi bir kullanım yoktur. Bu operatörün negatiften pozitife çeviren bir alternatifi bulumuyor.
 Buna benzer bir yazımda hata vericektir.

 "var b = -10;
 print(+b);"
 Bu hatalı bir kullanımdır.
 */
}
