void main() {
  var value = 10;
  print(~value);
  /*
  ---"~"---
  "~" operatörü ise değerin binary olarak tersini alır.
  Sayısal olarak tersini alır. ~x = -(x + 1) olarak ta yazılabilir. 
  00001010 = 10'dur. -11'in tersi ise binary olarak:
  11110101 = -10'dur. Buna 1 ekleyip yazdırılıyor yani;
  11110110 = -11 dir.
  */
}
