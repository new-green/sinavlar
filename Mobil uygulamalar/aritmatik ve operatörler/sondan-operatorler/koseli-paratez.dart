void main() {
  /*
  -------Köşeli parantezler-------
  Köşeli parantezler diğer dillerde de kullanıldığı gibi kullanılır.
  Liste ve haritalandırmada kullanlılır.
  Örneğin:
  */
  var list = [10, 20, 30, 40];
  print(list[1]);
  print(list.last);
  list.add(50);
  print(list[4]);

  /*
  Haritalama, haritalama bir sözlük dizisinde kullanılır.
  Süslü parantezlerle belirtilmiş sözlüklerde belirli bir elemanı çağırmak için kullanılır
  Örneğin: 
  */
  var map = {'name': 'Ahmet', 'age': 30};
  print(map['name']);
}
